# Wrapper function that follows the same pattern as other norm scripts
norm_stroop <- function(df) {
    # Create default T-score columns if they don't exist
    for (col in c("STRWT", "STRCT", "STRCWT", "STRIT")) {
        if (!(col %in% names(df))) {
            df[[col]] <- NA_real_
        }
    }
  
  # Apply rounding to Stroop variables
  df <- df %>%
    mutate(
        STRWRAW = round(STRWRAW, 0),
        STRCRAW = round(STRCRAW, 0),
        STRCWRAW = round(STRCWRAW, 0),
        STRWPRED = round(STRWPRED, 0),
        STRCPRED = round(STRCPRED, 0),
        STRCWPRED = round(STRCWPRED, 0)
    )
  
  # Call the main implementation function
  df <- stroop_norms(df)
  return(df)
}

